package Final;

public class Principal {
    /*
        Colocar a palavra final quando posta em uma classe evita que a classe sera herdada!
        e quando colocada sobre um metodo, evita que esse metodo seja sobrescrito

        porquê usar o Final ?

        por segurança:
            Dependendo do contexto do seu projeto é aconselhavel deixar a classe/metodo como final para protege-la de alterações indesejadas
            (normalmente é aconselhado colocar o final em metodos sobrescritos para evitar futuros problemas)
            (não é boa praticas sobrescrever um metodo sobrescrito)

        por performance:
            Os atributos de uma classe final são analisadas de forma mas rapida!


     */
}
